package ControlInfo;



public class CtrlBtn {
	public String name;
	private int x,y;
	private String function;
	private int element;
	
	public boolean onmap = false;
	
	public int buttonNo;
	
	public static int BUTTONOFFSET =7;
	
	public CtrlBtn(){
		
	}
	public CtrlBtn(int newX, int newY){
		
		x=newX;
		y=newY+BUTTONOFFSET;
		
	}
	public CtrlBtn(String newName, int newX, int newY, String newFunction,int newElement){
		name=newName;
		x=newX;
		y=newY+BUTTONOFFSET;
		function=newFunction;
		element = newElement; 
	}
	public int getX(){
		return x;
	}
	public int getY(){
		return y;
	}
	public String getName(){
		return name;
	}
	public String getFunction(){
		return function;
	}
	public int getElement(){
		return element;
	}
	

}
